// for declaring variables you should use let and const but you could use var as well, just don't
let a = 5;
// variables are loosely typed, meaning you can put anything in the variable that you want
a = "hello";

const c = 10;
// const cannot be reasigned once created

var v = "Don't use var";

console.log("a = " + a);  
console.log("c = " + c);
console.log("v = " + v);